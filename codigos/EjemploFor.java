public class EjemploFor {
    
    public static void main(String[] args) {
        // El ciclo inicia en 1, se repite mientras i sea menor o igual a 10
        // y en cada repetición aumenta i en 1
        for(int i = 1; i <= 10; i++) {
            System.out.println(i);  // Imprime el valor actual de i
        }
    }
}